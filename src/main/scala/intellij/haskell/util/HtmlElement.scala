package intellij.haskell.util

object HtmlElement {
  final val Quot = "&quot;"
  final val Lt = "&lt;"
  final val Gt = "&gt;"
  final val Amp = "&amp;"
  final val Nbsp = "&nbsp;"
  final val HtmlStart = "<html>"
  final val BodyStart = "<body>"
  final val HtmlEnd = "</html>"
  final val BodyEnd = "</body>"
  final val Break = "<br>"
  final val HorizontalLine = "<hr>"
  final val PreStart = "<pre>"
  final val PreEnd = "</pre>"
}
